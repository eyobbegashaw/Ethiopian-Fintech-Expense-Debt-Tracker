// Get the current locale from the device
 

// 1. የቋንቋ መለያ ፋንክሽን
const getLocale = () => {
  const locales = Localization.getLocales();
  // የመጀመሪያውን ቋንቋ እንወስዳለን፣ ካልተገኘ 'en' እንጠቀማለን
  const languageCode = locales[0]?.languageCode || 'en';
  
  if (languageCode.startsWith('am')) return 'am';
  return 'en';
};

